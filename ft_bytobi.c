#include "./includes/libft.h"

const char			*ft_bytobi(unsigned char c)
{
	static char		r[9];
	size_t			z;

	r[0] = 0;
	z = 128;
	while (z > 0)
	{
		ft_strcat(r, ((c & z) == z) ? "1" : "0");
		z >>= 1;
	}
	return (r);
}
