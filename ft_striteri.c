#include "libft.h"

void				ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	k;

	if (s != NULL)
	{
		k = 0;
		while (s[k])
		{
			f(k, &s[k]);
			++k;
		}
	}
}
